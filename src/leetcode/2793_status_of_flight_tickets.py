import pandas as pd
from more_itertools import unzip


def ticket_status(flights: pd.DataFrame, passengers: pd.DataFrame) -> pd.DataFrame:
    table = pd.merge(passengers, flights, on='flight_id')
    table.sort_values(by=['flight_id', 'booking_time'], inplace=True)
    status = {}
    prev_flight_id = table.iloc[0]['flight_id']
    count = 0
    for i in range(len(table['flight_id'])):
        curr_flight_id = table.iloc[i]['flight_id']
        passenger_id = table.iloc[i]['passenger_id']
        if curr_flight_id != prev_flight_id:
            count = 0
            prev_flight_id = curr_flight_id
        count += 1
        status[passenger_id] = ('Confirmed' if count <= table.iloc[i]['capacity'] else 'Waitlist')
    ids, stati = unzip(status.items())
    result = pd.DataFrame({'passenger_id': ids, 'Status': stati})
    result.sort_values(by='passenger_id', inplace=True)
    return result


